package com.th.th1.questions;

public class HashtagVO {

	private Integer hashtag_num;
	private String hashtag_name;
	private String hashtag_category;
	
	
	public Integer getHashtag_num() {
		return hashtag_num;
	}
	public void setHashtag_num(Integer hashtag_num) {
		this.hashtag_num = hashtag_num;
	}
	public String getHashtag_name() {
		return hashtag_name;
	}
	public void setHashtag_name(String hashtag_name) {
		this.hashtag_name = hashtag_name;
	}
	public String getHashtag_category() {
		return hashtag_category;
	}
	public void setHashtag_category(String hashtag_category) {
		this.hashtag_category = hashtag_category;
	}
	
	
}
