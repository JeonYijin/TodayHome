package com.th.th1.pr;



public class PrVO {
	private Integer pr_number;
	private Integer pr_page;
	private String pr_name;
	private Integer pr_price;
	private String img_url;
	public String getImg_url() {
		return img_url;
	}
	public void setImg_url(String img_url) {
		this.img_url = img_url;
	}
	public Integer getPr_number() {
		return pr_number;
	}
	public void setPr_number(Integer pr_number) {
		this.pr_number = pr_number;
	}
	public Integer getPr_page() {
		return pr_page;
	}
	public void setPr_page(Integer pr_page) {
		this.pr_page = pr_page;
	}
	public String getPr_name() {
		return pr_name;
	}
	public void setPr_name(String pr_name) {
		this.pr_name = pr_name;
	}
	public Integer getPr_price() {
		return pr_price;
	}
	public void setPr_price(Integer pr_price) {
		this.pr_price = pr_price;
	}
	

}
